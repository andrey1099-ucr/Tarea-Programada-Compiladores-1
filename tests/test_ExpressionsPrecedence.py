# Arithmetic precedence tests
a = 2 + 3 * 4          # ('+', 2, ('*',3,4))
b = 2 * 3 + 4          # ('+', ('*',2,3), 4)
c = 2 ** 3 ** 2        # ('**', 2, ('**',3,2))
d = -2 ** 3            # ('neg', ('**', ('num',2), ('num',3)))
e = (-2) ** 3          # ('**', ('neg', ('num',2)), ('num',3))
f = 2 + -3 * +4        # ('+', 2, ('*', ('neg',3), ('pos',4)))
g = (1 + 2) * 3        # ('*', ('+',1,2), 3)
h = 10 // 3 % 2        # ('%', ('//',10,3), 2)
i = 8 / 4 / 2          # ('/', ('/',8,4), 2)  left-assoc
j = 2 ** 3 * 4         # ('*', ('**',2,3), 4)